package Day03;

public class ComparisionEx {
    public static void main(String[] args) {

        //==,!=,>,<,=>,<=

        //always compare two variables or values in relational or comparision operator

        int a = 100;
        int b=200;
        System.out.println(a==b);//false
        System.out.println(a!=b);//true
        System.out.println(a>b);//false
        System.out.println(a<b);//true
        System.out.println(a>=b);//false
        System.out.println(a<=b);//true
    }
}
